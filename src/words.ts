export function getWordOfTheDay(wo: number) {
  let day = 0;
  if(wo < 0){
    const now = new Date()
    const start = new Date(2022, 0, 1)
    const diff = Number(now) - Number(start)
    day = Math.floor(diff / (1000 * 60 * 60 * 24))
    while (day > answers.length) {
      day -= answers.length
   }
  }else{
    day = wo % 12000
  }

  //console.log(answers[day])
  return answers[day]
}

